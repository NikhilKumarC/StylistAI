# Define tools for the conversational agent
@tool
def get_weather_info(location: str, date: str = "today") -> dict:
    """
    Get weather information for a specific location and date.

    Use this tool when:
    - User mentions a location (city, place, area)
    - You need weather to make appropriate outfit recommendations
    - User asks about weather

    Args:
        location: City or location name (e.g., "New York", "Miami", "Seattle")
        date: When they need weather (e.g., "today", "tomorrow", "this weekend")

    Returns:
        Weather information including temperature, conditions, and styling advice
    """
    try:
        logger.info(f"[Weather Tool] Fetching weather for {location} on {date}")
        weather = WeatherService.get_weather_for_location_and_date(location, date)

        if not weather:
            return {
                "error": f"Could not fetch weather for {location}",
                "suggestion": "Proceeding with general recommendations"
            }

        return {
            "location": location,
            "date": date,
            "temperature": weather.get("temperature", "Unknown"),
            "conditions": weather.get("conditions", "Unknown"),
            "description": weather.get("description", ""),
            "feels_like": weather.get("feels_like", ""),
            "humidity": weather.get("humidity", ""),
            "styling_advice": weather.get("styling_advice", "")
        }
    except Exception as e:
        logger.error(f"Error fetching weather: {e}")
        return {
            "error": str(e),
            "suggestion": "Recommend versatile layering options"
        }


