# Router: decide if we should continue to tools or end
def should_continue(state: ConversationState) -> str:
    """Determine if we should call tools or end"""
    messages = state['messages']
    last_message = messages[-1]

    # If LLM makes tool calls, route to tools node
    if hasattr(last_message, 'tool_calls') and last_message.tool_calls:
        logger.info(f"[Conversational Router] Routing to tools")
        return "tools"

    # Otherwise, done with this turn
    logger.info(f"[Conversational Router] Ending turn")
    return "end"


# Build the LangGraph workflow
def create_conversational_stylist_agent() -> StateGraph:
    """Create and compile the autonomous conversational stylist"""

    workflow = StateGraph(ConversationState)

    # Add nodes
    workflow.add_node("agent", conversational_agent_node)
    workflow.add_node("tools", tool_node)

    # Set entry point
    workflow.set_entry_point("agent")

    # Add conditional edges
    workflow.add_conditional_edges(
        "agent",
        should_continue,
        {
            "tools": "tools",
            "end": END
        }
    )

    # After tools, go back to agent for final response
    workflow.add_edge("tools", "agent")

    return workflow.compile()


# Main execution function
async def chat_with_stylist(
    user_id: str,
    user_message: str,
    conversation_history: Optional[List[dict]] = None,
    user_preferences: Optional[dict] = None
) -> dict:
    """
    Have a conversation with the autonomous stylist

    Args:
        user_id: User's ID
        user_message: User's latest message
        conversation_history: Previous messages in conversation
        user_preferences: User's profile from onboarding

    Returns:
        Agent's response and metadata
    """

    agent = create_conversational_stylist_agent()

    # Build messages from history
    messages = []
    if conversation_history:
        for msg in conversation_history[-10:]:  # Keep last 10 messages for context
            if msg.get("role") == "user":
                messages.append(HumanMessage(content=msg["content"]))
            elif msg.get("role") == "assistant":
                messages.append(AIMessage(content=msg["content"]))

    # Add current user message
    messages.append(HumanMessage(content=user_message))

    # Initial state
    initial_state: ConversationState = {
        "messages": messages,
        "user_id": user_id,
        "user_preferences": user_preferences
    }

    logger.info(f"\n{'='*50}")
    logger.info(f"Conversational Stylist - User: {user_id}")
    logger.info(f"Message: {user_message}")
    logger.info(f"{'='*50}\n")

    # Run agent
    final_state = await agent.ainvoke(initial_state)

    # Extract response
    final_messages = final_state['messages']

    # Get last AI message (skip tool messages and system messages)
    ai_response = None
    for msg in reversed(final_messages):
        if isinstance(msg, AIMessage) and hasattr(msg, 'content') and msg.content:
            # Make sure it's not a tool call message
            if not (hasattr(msg, 'tool_calls') and msg.tool_calls):
                ai_response = msg.content
                break

    # Check what tools were used - PURE SEQUENTIAL PATTERN
    tools_used = []
    weather_info = None

    for msg in final_messages:
        if isinstance(msg, ToolMessage):
            # Track which tool was called
            tool_name = msg.name if hasattr(msg, 'name') else 'unknown'
            tools_used.append(tool_name)

            # Extract data from tool results
            try:
                if isinstance(msg.content, str):
                    result = eval(msg.content)  # Tool results are string representations
                else:
                    result = msg.content

                # ONLY weather tool exists in pure sequential pattern
                if tool_name == 'get_weather_info' and not result.get('error'):
                    weather_info = result

            except Exception as e:
                logger.error(f"Error parsing tool result: {e}")

    # Determine if more context is needed - MULTI-SIGNAL DETECTION
    # More robust than just checking weather!

    # Signal 1: Weather has been fetched
    has_weather = weather_info is not None

    # Signal 2: Agent indicates readiness through response language
    # Look for phrases that indicate agent is ready to find outfits
    readiness_phrases = [
        "let me find",
        "i'll find",
        "let me get",
        "i've got all",
        "i have all",
        "perfect! let me",
        "great! let me",
        "i can help you with that",
        "here's what i suggest",
        "let me put together",
        "i'll put together"
    ]
    agent_signals_ready = any(
        phrase in ai_response.lower()
        for phrase in readiness_phrases
    ) if ai_response else False

    # Signal 3: Minimum conversation depth (at least 1-2 exchanges)
    # Prevents weather-only queries from triggering recommendations
    min_conversation_depth = len(conversation_history) >= 2  # At least 1 user msg + 1 assistant msg before this

    # Context is complete when:
    # 1. Weather fetched (indicates location + when)
    # 2. Agent signals readiness in response
    # 3. Had at least minimal conversation (not just "what's the weather?")
    needs_more_context = not (has_weather and agent_signals_ready and min_conversation_depth)

    logger.info(f"[Context Detection] has_weather={has_weather}, agent_ready={agent_signals_ready}, min_depth={min_conversation_depth}")
    logger.info(f"[Context Detection] needs_more_context={needs_more_context}")

    # PURE SEQUENTIAL: No recommendations here!
    # API will call langgraph agent when context is complete
    return {
        "response": ai_response or "I'm here to help you look great! What can I help you with?",
        "recommendations": [],  # Always empty - API orchestrates recommendations
        "weather_info": weather_info,
        "tools_used": tools_used,
        "has_recommendations": False,  # Always False - recommendations come from API
        "needs_more_context": needs_more_context,  # Multi-signal detection
        "context_signals": {  # Debug info for UI/logging
            "has_weather": has_weather,
            "agent_signals_ready": agent_signals_ready,
            "min_conversation_depth": min_conversation_depth
        }
    }


